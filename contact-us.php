<script type="text/javascript">
    $('#desktop_contact').submit(function(e) {
        e.preventDefault();

        var form_data = new FormData($(this)[0]);
        var base_url = $('#base_url').val();

        $.ajax({
            type: 'post',
            data: form_data,
            processData: false,
            contentType: false,
            url: base_url+'enquiry.php',
            success: function(data) {
                var obj = $.parseJSON(data);

                if (obj.errorFlag == false) {
                    alert(obj.message);
                    window.location.reload();
                } else if (obj.errorFlag == true) {
                    $('.error').remove();
                    $.each(obj.messages, function(key, value) {
                        var element = $('#' + key);
                        value = '<span class="error">'+value+'</span>';
                        element.closest('.meInput').after(value);


                    });
                }

            }

        });

    });
</script>
